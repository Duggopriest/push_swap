#include "push_swap.h"

void	sort_3(t_stacks *sk)
{
	if (sk->a[1] == 1 && sk->a[2] == 2)
		swapa(sk);
	else if (sk->a[0] == 1 && sk->a[2] == 2)
		rotatea(sk);
	else if (sk->a[2] == 3 && sk->a[0] == 1)
	{
		rotatea(sk);
		swapa(sk);
	}
}